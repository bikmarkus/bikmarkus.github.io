const TOTAL_QUESTIONS = 75;
let currentIdx = 0;
let score = 0;
let timeLeft = 15;
let timerInterval;
let shuffledQuestions = [];

function init() {
    shuffledQuestions = [...questionsData].sort(() => Math.random() - 0.5).slice(0, TOTAL_QUESTIONS);
    render();
}

function render() {
    if (currentIdx >= shuffledQuestions.length) return end();

    clearInterval(timerInterval);
    timeLeft = 15;
    updateTimerUI();
    startTimer();

    const qData = shuffledQuestions[currentIdx];
    document.getElementById('question-number').innerText = `Вопрос ${currentIdx + 1}/${TOTAL_QUESTIONS}`;
    document.getElementById('question-text').innerText = qData.q;

    const options = [qData.a, ...qData.o].sort(() => Math.random() - 0.5);
    const container = document.getElementById('options-container');
    container.innerHTML = '';

    options.forEach(opt => {
        const btn = document.createElement('button');
        btn.innerText = opt;
        btn.onclick = () => check(opt === qData.a);
        container.appendChild(btn);
    });

    document.getElementById('progress-bar').style.width = (currentIdx / TOTAL_QUESTIONS * 100) + "%";
}

function startTimer() {
    timerInterval = setInterval(() => {
        timeLeft--;
        updateTimerUI();
        if (timeLeft <= 0) check(false);
    }, 1000);
}

function updateTimerUI() {
    const timerEl = document.getElementById('timer');
    const wrapper = document.getElementById('timer-wrapper');
    timerEl.innerText = timeLeft;
    
    if (timeLeft <= 5) {
        wrapper.classList.add('timer-low');
    } else {
        wrapper.classList.remove('timer-low');
    }
}

function check(isCorrect) {
    if (isCorrect) score++;
    currentIdx++;
    render();
}

function end() {
    clearInterval(timerInterval);
    document.getElementById('progress-bar').style.width = "100%";
    const box = document.getElementById('question-box');
    
    box.innerHTML = `
        <div style="text-align:center">
            <h2>Результаты экспедиции</h2>
            <p style="font-size: 2rem; margin: 15px 0;">${score} / ${TOTAL_QUESTIONS}</p>
            <button onclick="location.reload()">Перезапуск</button>
        </div>
    `;
    
    if (score === TOTAL_QUESTIONS) launch();
}

function launch() {
    const r = document.createElement('div');
    r.className = 'rocket';
    r.innerText = '🚀';
    document.getElementById('rocket-container').appendChild(r);
}

init();