let selectedQuestions = [];
let userAnswers = []; 
let currentIdx = 0;
let score = 0;
let timeLeft = 20;
let timerInterval;

function startQuiz() {
    const count = parseInt(document.getElementById('question-count').value);
    selectedQuestions = [...questionsData].sort(() => Math.random() - 0.5).slice(0, count);
    
    userAnswers = [];
    currentIdx = 0;
    score = 0;
    
    document.getElementById('start-screen').style.display = 'none';
    document.getElementById('results-screen').style.display = 'none';
    document.getElementById('game-screen').style.display = 'block';
    
    render();
}

function render() {
    if (currentIdx >= selectedQuestions.length) return end();

    clearInterval(timerInterval);
    timeLeft = 20;
    updateTimerUI();
    startTimer();

    const q = selectedQuestions[currentIdx];
    document.getElementById('question-number').innerText = `Вопрос ${currentIdx + 1}/${selectedQuestions.length}`;
    document.getElementById('question-text').innerText = q.q;

    const options = [q.a, ...q.o].sort(() => Math.random() - 0.5);
    const container = document.getElementById('options-container');
    container.innerHTML = '';

    options.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.innerText = opt;
        btn.onclick = () => check(opt, q.a);
        container.appendChild(btn);
    });

    document.getElementById('progress-bar').style.width = (currentIdx / selectedQuestions.length * 100) + "%";
}

function startTimer() {
    timerInterval = setInterval(() => {
        timeLeft--;
        updateTimerUI();
        if (timeLeft <= 0) {
            check(null, selectedQuestions[currentIdx].a);
        }
    }, 1000);
}

function updateTimerUI() {
    const t = document.getElementById('timer');
    const w = document.getElementById('timer-wrapper');
    if (t) t.innerText = timeLeft;
    if (timeLeft <= 5) {
        w.classList.add('timer-low');
    } else {
        w.classList.remove('timer-low');
    }
}

function check(selected, correct) {
    const isCorrect = selected === correct;
    if (isCorrect) score++;
    
    userAnswers.push({
        question: selectedQuestions[currentIdx].q,
        isCorrect: isCorrect,
        userAnswer: selected || "Время вышло",
        correctAnswer: correct
    });

    currentIdx++;
    render();
}

function end() {
    clearInterval(timerInterval);
    document.getElementById('game-screen').style.display = 'none';
    document.getElementById('results-screen').style.display = 'block';
    
    document.getElementById('score-summary').innerHTML = `
        <p style="font-size: 1.8rem; margin: 10px 0;">Счет: ${score} / ${selectedQuestions.length}</p>
    `;

    const statsContainer = document.getElementById('detailed-stats');
    statsContainer.innerHTML = '';

    userAnswers.forEach((ans, index) => {
        const div = document.createElement('div');
        div.className = 'stat-item';
        const statusClass = ans.isCorrect ? 'correct-stat' : 'wrong-stat';
        const statusText = ans.isCorrect ? 'ВЕРНО' : 'ОШИБКА';
        
        div.innerHTML = `
            <div class="${statusClass}"><span>#${index + 1}</span> ${statusText}</div>
            <div>${ans.question}</div>
            <div style="font-size: 0.85rem; opacity: 0.8;">Ваш ответ: ${ans.userAnswer} | Правильный: ${ans.correctAnswer}</div>
        `;
        statsContainer.appendChild(div);
    });

    if (score === selectedQuestions.length && selectedQuestions.length > 0) {
        launch();
    }
}

function launch() {
    const rocketContainer = document.getElementById('rocket-container');
    if (!rocketContainer) return;
    
    const r = document.createElement('div');
    r.className = 'rocket';
    r.innerText = '🚀';
    rocketContainer.appendChild(r);
    
    setTimeout(() => r.remove(), 4000);
}