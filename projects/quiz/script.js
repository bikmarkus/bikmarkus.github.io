let selectedQuestions = [];
let currentIdx = 0;
let score = 0;
let timeLeft = 20;
let timerInterval;

function startQuiz() {
    const count = parseInt(document.getElementById('question-count').value);
    selectedQuestions = [...questionsData].sort(() => Math.random() - 0.5).slice(0, count);
    
    document.getElementById('start-screen').style.display = 'none';
    document.getElementById('game-screen').style.display = 'block';
    
    render();
}

function render() {
    if (currentIdx >= selectedQuestions.length) return end();

    clearInterval(timerInterval);
    timeLeft = 20;
    updateTimerUI();
    startTimer();

    const q = selectedQuestions[currentIdx];
    document.getElementById('question-number').innerText = `Вопрос ${currentIdx + 1}/${selectedQuestions.length}`;
    document.getElementById('question-text').innerText = q.q;

    const options = [q.a, ...q.o].sort(() => Math.random() - 0.5);
    const container = document.getElementById('options-container');
    container.innerHTML = '';

    options.forEach(opt => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.innerText = opt;
        btn.onclick = () => check(opt === q.a);
        container.appendChild(btn);
    });

    document.getElementById('progress-bar').style.width = (currentIdx / selectedQuestions.length * 100) + "%";
}

function startTimer() {
    timerInterval = setInterval(() => {
        timeLeft--;
        updateTimerUI();
        if (timeLeft <= 0) check(false);
    }, 1000);
}

