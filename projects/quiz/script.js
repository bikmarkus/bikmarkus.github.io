let currentIdx = 0;
let score = 0;
let timeLeft = 30;
let timerInterval;
let shuffledQuestions = [];

function init() {
    // Перемешиваем вопросы
    shuffledQuestions = [...questionsData].sort(() => Math.random() - 0.5);
    renderQuestion();
}

function renderQuestion() {
    if (currentIdx >= shuffledQuestions.length) return endGame();

    // Сброс таймера на каждый вопрос
    clearInterval(timerInterval);
    timeLeft = 30;
    document.getElementById('timer').innerText = timeLeft;
    startTimer();

    const q = shuffledQuestions[currentIdx];
    document.getElementById('question-number').innerText = `Вопрос ${currentIdx + 1}/25`;
    document.getElementById('question-text').innerText = q.q;

    // Перемешиваем варианты: правильный + неправильные
    const allOptions = [q.a, ...q.o].sort(() => Math.random() - 0.5);
    
    const container = document.getElementById('options-container');
    container.innerHTML = '';

    allOptions.forEach(opt => {
        const btn = document.createElement('button');
        btn.innerText = opt;
        btn.onclick = () => handleAnswer(opt === q.a);
        container.appendChild(btn);
    });

    updateProgress();
}

function startTimer() {
    timerInterval = setInterval(() => {
        timeLeft--;
        document.getElementById('timer').innerText = timeLeft;
        if (timeLeft <= 0) {
            handleAnswer(false); // Автоматический проигрыш вопроса при истечении времени
        }
    }, 1000);
}

function handleAnswer(isCorrect) {
    if (isCorrect) score++;
    currentIdx++;
    renderQuestion();
}

function updateProgress() {
    const percent = (currentIdx / shuffledQuestions.length) * 100;
    document.getElementById('progress-bar').style.width = percent + "%";
}

function endGame() {
    clearInterval(timerInterval);
    const box = document.getElementById('question-box');
    box.innerHTML = `
        <div style="text-align:center">
            <h2>Миссия завершена!</h2>
            <p style="font-size: 1.5rem">Ваш результат: ${score} из 25</p>
            <button onclick="location.reload()" style="text-align:center">Повторить миссию</button>
        </div>
    `;
    
    if (score === 25) {
        launchRocket();
    }
}

function launchRocket() {
    const r = document.createElement('div');
    r.className = 'rocket';
    r.innerText = '🚀';
    document.getElementById('rocket-container').appendChild(r);
}

init();